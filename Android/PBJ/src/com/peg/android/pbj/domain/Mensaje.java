package com.peg.android.pbj.domain;

public class Mensaje {

    String link;
    String texto;
    int time;
    
    public String getLink() {
            return link;
    }
    public void setLink(String link) {
            this.link = link;
    }
    public String getTexto() {
            return texto;
    }
    public void setTexto(String texto) {
            this.texto = texto;
    }
    public int getTime() {
            return time;
    }
    public void setTime(int time) {
            this.time = time;
    }
    
}
